package com.fssa.agrokart.model;

/**
 * Represents the gender of a user.
 */
public enum Gender {

    /**
     * Represents the male gender.
     */
    MALE,

    /**
     * Represents the female gender.
     */
    FEMALE,

    /**
     * Represents a gender other than male or female.
     */
    OTHERS
}
