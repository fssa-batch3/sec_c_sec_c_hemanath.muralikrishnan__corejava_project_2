package com.fssa.agrokart.util;

import java.util.logging.Level;

/**
 * A utility class for printing object strings to the output stream. This class
 * provides a simple way to print object information to the console.
 *
 * @author HemanathMuralikrishnan
 */
public class Logger {

	/**
	 * Print the string representation of an object to the standard output stream.
	 * This method is useful for logging information and debugging purposes.
	 *
	 * @param obj The object whose string representation needs to be printed.
	 */
	public void info(Object obj) {
		System.out.println(obj);
	}
}
